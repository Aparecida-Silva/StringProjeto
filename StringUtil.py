def output(texto):
    print(texto)

def tamanho(texto):
    tamanho = len(texto)
    print ("Tamanho do Texto: ", tamanho)
    return tamanho

def maiusculo(texto):
    maiusculo = texto.upper()
    print ("Maiúsculo: ", maiusculo)
    return maiusculo

def minusculo(texto):
    minusculo = texto.lower
    print ("Minúsculo: ", minusculo)
    return minusculo
